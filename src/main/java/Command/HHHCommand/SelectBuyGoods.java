package Command.HHHCommand;

public class SelectBuyGoods
{
	private String goodPlaceName;
	private String goodPlaceNum;
	private String companyNum;
	private String buyStartDate;
	private String buyEndDate;
	private String buyDays;
	private String totalPrice;
	
	public String getGoodPlaceName()
	{
		return goodPlaceName;
	}
	public void setGoodPlaceName(String goodPlaceName)
	{
		this.goodPlaceName = goodPlaceName;
	}
	public String getGoodPlaceNum()
	{
		return goodPlaceNum;
	}
	public void setGoodPlaceNum(String goodPlaceNum)
	{
		this.goodPlaceNum = goodPlaceNum;
	}
	public String getCompanyNum()
	{
		return companyNum;
	}
	public void setCompanyNum(String companyNum)
	{
		this.companyNum = companyNum;
	}
	public String getBuyStartDate()
	{
		return buyStartDate;
	}
	public void setBuyStartDate(String buyStartDate)
	{
		this.buyStartDate = buyStartDate;
	}
	public String getBuyEndDate()
	{
		return buyEndDate;
	}
	public void setBuyEndDate(String buyEndDate)
	{
		this.buyEndDate = buyEndDate;
	}
	public String getBuyDays()
	{
		return buyDays;
	}
	public void setBuyDays(String buyDays)
	{
		this.buyDays = buyDays;
	}
	public String getTotalPrice()
	{
		return totalPrice;
	}
	public void setTotalPrice(String totalPrice)
	{
		this.totalPrice = totalPrice;
	}
	
	
	
}
