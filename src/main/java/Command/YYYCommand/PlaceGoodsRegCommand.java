package Command.YYYCommand;

public class PlaceGoodsRegCommand {
	String companyName; //업체명
	String goodsName; //상품명
	String goodsImgName; //메인이미지
	String goodsDetail; // 상세내용
	String goodsDanger; //주의사항
	Integer goodsStock; //수량
	Integer goodsPrice; //상품금액
	
	
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public String getGoodsImgName() {
		return goodsImgName;
	}
	public void setGoodsImgName(String goodsImgName) {
		this.goodsImgName = goodsImgName;
	}
	public String getGoodsDetail() {
		return goodsDetail;
	}
	public void setGoodsDetail(String goodsDetail) {
		this.goodsDetail = goodsDetail;
	}
	public String getGoodsDanger() {
		return goodsDanger;
	}
	public void setGoodsDanger(String goodsDanger) {
		this.goodsDanger = goodsDanger;
	}
	public Integer getGoodsStock() {
		return goodsStock;
	}
	public void setGoodsStock(Integer goodsStock) {
		this.goodsStock = goodsStock;
	}
	public Integer getGoodsPrice() {
		return goodsPrice;
	}
	public void setGoodsPrice(Integer goodsPrice) {
		this.goodsPrice = goodsPrice;
	}
	
	
	
}